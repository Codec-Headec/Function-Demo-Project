package multifunct;
import java.lang.*;

public class MultiFunct{
    public static void main(String[] args) {
        System.out.println("I am in main");
        itly();
        brazil();
        India();
    }
    static void itly(){
        System.out.println("I am in Itly");
    }

    static void brazil(){
        System.out.println("I am in Brazil");
    }

    static void India(){
        System.out.println("I am in India");
    }
}